    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        int codigo;

        if (!txtCodigoParcela.getText().equals("")) {
            codigo = Integer.parseInt(txtCodigoParcela.getText());
            mod.setCodParcela(codigo);
            mod = control.BuscaParcela(mod);
            txtCodigoParcela.setText(String.valueOf(mod.getCodParcela()));
            txtCodigoVenda.setText(String.valueOf(mod.getCodVenda()));
            txtValor.setText(String.valueOf(mod.getValorVenda()));
            txtVencimento.setText(mod.getDataVencimento());
            preencherTabela("select * from parcela_venda where cod_venda ='" + txtCodigoVenda.getText() + "'order by id_parcela_venda");

        } else {
            JOptionPane.showMessageDialog(rootPane, "Informe um Numero de Parcela!!");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnBaixarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBaixarActionPerformed
        mod.setCodParcela(Integer.parseInt(txtCodigoParcela.getText()));
        control.BaixarParcela(mod);
        preencherTabela("select * from parcela_venda where cod_venda ='" + txtCodigoVenda.getText() + "'order by id_parcela_venda");

    }//GEN-LAST:event_btnBaixarActionPerformed

    public void preencherTabela(String sql) {
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"Parcela", "Venda", "Valor", "Estado"};

        ConnBaixa.executaSQL(sql);
        try {
            ConnBaixa.rs.first();
            do {
                dados.add(new Object[]{ConnBaixa.rs.getInt("id_parcela_venda"), ConnBaixa.rs.getString("cod_venda"), ConnBaixa.rs.getString("valor_parcela"), ConnBaixa.rs.getString("estado")});
            } while (ConnBaixa.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "NÃ£o Contem Bairro Cadastrado no Banco de Dados  !");
        }

        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        tblBaixa.setModel(modelo);
        tblBaixa.getColumnModel().getColumn(0).setPreferredWidth(86);
        tblBaixa.getColumnModel().getColumn(0).setResizable(false);
        tblBaixa.getColumnModel().getColumn(1).setPreferredWidth(95);
        tblBaixa.getColumnModel().getColumn(1).setResizable(false);
        tblBaixa.getColumnModel().getColumn(2).setPreferredWidth(108);
        tblBaixa.getColumnModel().getColumn(2).setResizable(false);
        tblBaixa.getColumnModel().getColumn(3).setPreferredWidth(107);
        tblBaixa.getColumnModel().getColumn(3).setResizable(false);
        tblBaixa.getTableHeader().setResizingAllowed(false);
        tblBaixa.setAutoResizeMode(tblBaixa.AUTO_RESIZE_OFF);
        tblBaixa.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

    }

