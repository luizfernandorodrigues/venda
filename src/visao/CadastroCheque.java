    private void btnBuscaClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscaClienteActionPerformed
        connCheque.conexao();

        try {
            connCheque.executaSQL("SELECT * FROM CLIENTES WHERE NOME_CLIENTE LIKE '%" + txtCodigo.getText() + "%'");
            connCheque.rs.first();
            txtCliente.setText(connCheque.rs.getString("nome_cliente"));
            txtCodigo.setText(connCheque.rs.getString("cpf_cliente"));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Nenhum Registro Encontrado!\n" + ex);
        }
        connCheque.desconecta();
    }//GEN-LAST:event_btnBuscaClienteActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (flag == 1) {
            mod.setAgenciaCheque(txtAgencia.getText());
            mod.setBancoCheque(txtBanco.getText());
            mod.setContaCheque(txtContaCheque.getText());
            mod.setEmissaoCheque(txtfEmissao.getText());
            mod.setIdCliente(txtCodigo.getText());
            mod.setNomeTerceiro(txtTitularCheque.getText());
            mod.setNumeroCheque(Integer.parseInt(txtNumeroCheque.getText()));
            mod.setObservacaoCheuque(txtObservacao.getText());
            mod.setStatusCheque((String) cbxStatus.getSelectedItem());
            mod.setValorCheque(Double.parseDouble(txtValor.getText()));
            mod.setVencimentoCheque(txtfVencimento.getText());
            control.inserir(mod);
        } else {
            mod.setIdCheque(Integer.parseInt(txtCodigoCheque.getText()));
            mod.setAgenciaCheque(txtAgencia.getText());
            mod.setBancoCheque(txtBanco.getText());
            mod.setContaCheque(txtContaCheque.getText());
            mod.setEmissaoCheque(txtfEmissao.getText());
            mod.setIdCliente(txtCodigo.getText());
            mod.setNomeTerceiro(txtTitularCheque.getText());
            mod.setNumeroCheque(Integer.parseInt(txtNumeroCheque.getText()));
            mod.setObservacaoCheuque(txtObservacao.getText());
            mod.setStatusCheque((String) cbxStatus.getSelectedItem());
            mod.setValorCheque(Double.parseDouble(txtValor.getText()));
            mod.setVencimentoCheque(txtfVencimento.getText());
            control.alterar(mod);
        }
        btnExcluir.setEnabled(true);
        util.desabilitaCampos(this);
        txtPesquisa.setEnabled(true);
    }//GEN-LAST:event_btnSalvarActionPerformed

