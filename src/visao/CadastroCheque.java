    public void preencherTabela(String sql) {
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"N° CHEQUE", "EMISSÃO", "VENCIMENTO", "STATUS"};
        connCheque.conexao();

        connCheque.executaSQL(sql);
        try {
            connCheque.rs.first();
            do {
                dados.add(new Object[]{connCheque.rs.getString("numero_cheque"), connCheque.rs.getString("emissao"), connCheque.rs.getString("vencimento_cheque"), connCheque.rs.getString("status_cheque")});
            } while (connCheque.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não Contem Cheque Cadastrado no Banco de Dados  !");
        }

        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        tblCheque.setModel(modelo);
        tblCheque.getColumnModel().getColumn(0).setPreferredWidth(100);
        tblCheque.getColumnModel().getColumn(0).setResizable(false);
        tblCheque.getColumnModel().getColumn(1).setPreferredWidth(100);
        tblCheque.getColumnModel().getColumn(1).setResizable(false);
        tblCheque.getColumnModel().getColumn(2).setPreferredWidth(115);
        tblCheque.getColumnModel().getColumn(2).setResizable(false);
        tblCheque.getColumnModel().getColumn(3).setPreferredWidth(233);
        tblCheque.getColumnModel().getColumn(3).setResizable(false);
        tblCheque.getTableHeader().setResizingAllowed(false);
        tblCheque.setAutoResizeMode(tblCheque.AUTO_RESIZE_OFF);
        tblCheque.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

    }

