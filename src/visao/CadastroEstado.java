    private void tblEstadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEstadoMouseClicked
        String descricaoEstado = "" + tblEstado.getValueAt(tblEstado.getSelectedRow(), 1);
        conecta.conexao();
        
        try {
            conecta.executaSQL("select * from estado where nome = '" + descricaoEstado + "' ");
            conecta.rs.first();
            txtCodigo.setText(String.valueOf(conecta.rs.getInt(("id_estado"))));
            txtNome.setText(conecta.rs.getString("nome"));
            txtSigla.setText(conecta.rs.getString("sigla_estado"));
            conecta.desconecta();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Não Foi Possivel Carregar o Registro!!");
        }
    }//GEN-LAST:event_tblEstadoMouseClicked

    public void preencherTabela(String sql) {
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"ID", "NOME", "SIGLA"};

        conecta.executaSQL(sql);
        try {
            conecta.rs.first();
            do {
                dados.add(new Object[]{conecta.rs.getInt("id_estado"), conecta.rs.getString("nome"), conecta.rs.getString("sigla_estado")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao Preencher o Array List !\n ERRO:  " + ex);
        }

        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        tblEstado.setModel(modelo);
        tblEstado.getColumnModel().getColumn(0).setPreferredWidth(102);
        tblEstado.getColumnModel().getColumn(0).setResizable(false);
        tblEstado.getColumnModel().getColumn(1).setPreferredWidth(250);
        tblEstado.getColumnModel().getColumn(1).setResizable(false);
        tblEstado.getColumnModel().getColumn(2).setPreferredWidth(150);
        tblEstado.getColumnModel().getColumn(2).setResizable(false);
        tblEstado.getTableHeader().setResizingAllowed(false);
        tblEstado.setAutoResizeMode(tblEstado.AUTO_RESIZE_OFF);
        tblEstado.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

    }
     

