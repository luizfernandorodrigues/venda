    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            PreparedStatement pst = conecta.conn.prepareStatement("insert into estado (nome, sigla_estado) values (?, ?)");// é a passagem do sql para inserçao dos dados
            if (txtNome.getText().equals("") || txtSigla.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Verifique os Campos !");

            } else {
                pst.setString(1, txtNome.getText());// passagem dos parametros
            }
            pst.setString(2, txtSigla.getText());// passagem de parametros
            {
                pst.execute();// execução da inserçao dos dados
                JOptionPane.showMessageDialog(rootPane, "Salvo com sucesso !");
                btnSalvar.setEnabled(false);
                preencherTabela("select * from estado order by nome");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao gravar os dados !\n ERRO !" + ex);

        }

        btnEditar.setEnabled(true);
        btnNovo.setEnabled(true);
        btnExcluir.setEnabled(true);
        txtCodigo.setText("");
        txtNome.setText("");
        txtSigla.setText("");
        txtCodigo.setEnabled(false);
        txtNome.setEnabled(false);
        txtSigla.setEnabled(false);

        cidade.ListaDeEstados();


    }//GEN-LAST:event_btnSalvarActionPerformed
