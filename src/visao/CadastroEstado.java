    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        btnNovo.setEnabled(true);
        btnExcluir.setEnabled(true);
        btnSalvar.setEnabled(true);
        btnEditar.setEnabled(true);
        try {

            PreparedStatement pst = conecta.conn.prepareStatement("update estado set nome = ?, sigla_estado = ? where id_estado = ?");
            pst.setString(1, txtNome.getText());
            pst.setString(2, txtSigla.getText());
            pst.setInt(3, Integer.parseInt(txtCodigo.getText()));
            pst.execute();
            JOptionPane.showMessageDialog(rootPane, "Registro Alterado com Sucesso  !");
            preencherTabela("select * from estado order by nome");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro na Alteração !\n ERRO: " + ex.getMessage());
        }

    }//GEN-LAST:event_btnEditarActionPerformed

